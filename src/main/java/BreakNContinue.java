public class BreakNContinue {
    public static void main(String[]args){
        /**
         * Continue keyword skips a run in a loop and run to the next one
         */
        for(int i=0;i<=9;i++){
            if(i%2 !=0){
                continue;

        }
            System.out.println(i);}
        /**
         * Break completely breaks the loop
         */
    }
}
