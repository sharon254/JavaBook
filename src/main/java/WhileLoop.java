public class WhileLoop {
    public static void main(String[]args){
        // keep running as long as a condition is true
        int numberT = 25;
        while(numberT <= 30){
            System.out.println(numberT);
            numberT++;
        }
        for(int number =25; number <= 30;number ++){
            System.out.println("Welcome!");
        }

    }
}
